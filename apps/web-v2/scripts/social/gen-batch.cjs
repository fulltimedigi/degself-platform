// schedule: 2/day — Kuwait 09:00 (06:00 UTC) + 21:00 (18:00 UTC), from tomorrow.
function schedule(posts) {
  const QF = path.join(process.cwd(), "scripts/content-queue.json");
  const start = new Date();
  start.setUTCHours(6, 0, 0, 0);
  start.setUTCDate(start.getUTCDate() + 1);
  const slotsUTC = [6, 18];
  const entries = posts.map((p, i) => {
    const d = new Date(start);
    d.setUTCDate(start.getUTCDate() + Math.floor(i / 2));
    d.setUTCHours(slotsUTC[i % 2], 0, 0, 0);
    return {
      id: path.basename(p.file).replace(/\.(jpg|mp4)$/, ""),
      status: "scheduled",
      publish_at: d.toISOString(),
      type: p.type,
      media_url: `https://degself.com/${p.file}`, // upload.cjs rewrites to the Supabase public URL (by basename)
      caption: p.caption,
    };
  });
  fs.writeFileSync(QF, JSON.stringify(entries, null, 2) + "\n");
  return entries;
}

(async () => {
  const posts = [];
  for (let i = 0; i < SPECS.length; i++) {
    const s = SPECS[i];
    const file = `s${String(i + 1).padStart(2, "0")}.jpg`;
    const f =
      s.t === "tip" ? await tipList({ tag: s.tag, title: s.title, items: s.items, footnote: s.foot, file })
      : s.t === "num" ? await bigNumber({ tag: s.tag, number: s.number, label: s.label, sub: s.sub, file })
      : s.t === "hero" ? await hero({ tag: s.tag, title: s.title, sub: s.sub, file })
      : await statement({ tag: s.tag, kicker: s.kicker, headline: s.headline, sub: s.sub, file });
    posts.push({ type: "image", file: f, caption: s.cap });
  }
  const entries = schedule(posts);
  console.log(`✅ rendered ${posts.length} images and queued ${entries.length} posts`);
  console.log(`   first: ${entries[0].publish_at}  ·  last: ${entries[entries.length - 1].publish_at}`);
})().catch((e) => { console.error("❌", e); process.exit(1); });
